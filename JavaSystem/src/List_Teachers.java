    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        // TODO add your handling code here:
       
       Lecturer[] eventsArr = new Lecturer[VIASystem.lecturers.getAllLecturers().size()];
       
       if(jComboBox1.getSelectedItem() == "Lecturer name")
       {
          
             eventsArr[0] = VIASystem.lecturers.getLecturer(jTextField1.getText());
           
           jList1.setListData(eventsArr);
       }
       else if(jComboBox1.getSelectedItem() == "Email")
       {
            eventsArr = VIASystem.lecturers.getLecturerByEmail(jTextField1.getText());
            jList1.setListData(eventsArr);
       }
       else if(jComboBox1.getSelectedItem() == "Phone")
       {
          eventsArr = VIASystem.lecturers.getLecturerByPhone(jTextField1.getText());
          jList1.setListData(eventsArr);
       }
       else if(jComboBox1.getSelectedItem() == "Sponsors")
       {
          
          eventsArr = VIASystem.lecturers.getLecturer(true);
          jList1.setListData(eventsArr);
       }
       else if(jComboBox1.getSelectedItem() == "Non-sponsors")
       {
          eventsArr = VIASystem.lecturers.getLecturer(false);
          jList1.setListData(eventsArr);
       }
       else if(jComboBox1.getSelectedItem() == "Subject")
       {
          eventsArr = VIASystem.lecturers.getLecturerBySubject(jTextField1.getText());
          jList1.setListData(eventsArr);
       }
    }//GEN-LAST:event_jTextField1ActionPerformed

