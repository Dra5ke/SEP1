    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) throws FileNotFoundException {//GEN-FIRST:event_jButton2ActionPerformed
        
       Lecturer lecturer = new Lecturer(1);
       lecturer = (Lecturer) jList1.getSelectedValue();
       String name = "";
       String email= "";
       String phone = "";
       String sponsor = "";
       String subject = "";

       name = JOptionPane.showInputDialog("Enter a new name: "
             + "\n(leave empty if you do not wish to modify it)");

       email = JOptionPane.showInputDialog("Enter a new email address: "
             + "\n(leave empty if you do not wish to modify it)");

       phone = JOptionPane.showInputDialog("Enter a new phone number: "
             + "\n(leave empty if you do not wish to modify it)");

       sponsor = JOptionPane.showInputDialog(
             "Is the lecturer a sponsor?(answer with yes or no) "
                   + "\n(leave empty if you do not wish to modify it)");

       subject = JOptionPane.showInputDialog("Enter the name of the lecturer's subject: "
             + "\n(leave empty if you do not wish to modify it)");

       if(name.length() != 0)
       {
          lecturer.setName(name);
       }
       if(email.length() != 0)
       {
          lecturer.setEmail(email);
       }
       if(phone.length() != 0)
       {
          lecturer.setPhone(phone);
       }
       if(sponsor.length() != 0)
       {
          if(sponsor.equals("yes") || sponsor.equals("Yes"))
          {
             lecturer.setSponsor(true);
          }
          else if(sponsor.equals("no") || sponsor.equals("No"))
          {
             lecturer.setSponsor(false);
          }
          else
          {
             JOptionPane.showMessageDialog(null, "Answer with yes or no for sponsorship status");
          }
       }
       if(subject.length() != 0)
       {
             lecturer.setSubject(subject);
       }
        
       lecturer.writeToFile();
       
    }//GEN-LAST:event_jButton2ActionPerformed

